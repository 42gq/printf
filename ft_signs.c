#include "ft_printf.h"

void	ft_signs(char *str, t_env *e)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && (str[i] == '0' || str[i] == ' '
				|| str[i] == '-' || str[i] == '+' || str[i] == '#'))
	{
		if (str[i] == ' ')
			e->space += 1;
		if (str[i] == '#')
			e->diese += 1;
		if (str[i] == '-')
			e->minus += 1;
		if (str[i] == '+')
			e->plus += 1;
		i++;
	}
}
